#include "3-calc.h"

/**
 * op_add - the function returns a sum
 * @a: first number
 * @b: second number
 * Return: the sum of the first and second number
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - this function returns the of difference a and b
 * @a: first number
 * @b: second number
 * Return: the difference of the first and second number
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - this function returns the product
 * @a: first number
 * @b: second number
 * Return: the product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - this function returns the quotient of a and b
 * @a: first number
 * @b: second number
 * Return: the quotiant of first and second number
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - this function returns the remainder
 * @a: first number
 * @b: second number
 * Return: the remainder of the first and second number
 */

int op_mod(int a, int b)
{
	return (a % b);
}
